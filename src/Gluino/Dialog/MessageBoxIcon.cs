namespace Gluino;

/// <summary>Specifies the icon that is displayed inside a message box.</summary>
public enum MessageBoxIcon
{
    /// <summary>The message box contains no icon.</summary>
    None,
    /// <summary>The message box contains an icon consisting of a white X in a circle with a red background.</summary>
    Error,
    /// <summary>The message box contains an icon consisting of an exclamation point in a triangle with a yellow background.</summary>
    Warning,
    /// <summary>The message box contains an icon consisting of a lowercase letter i in a circle.</summary>
    Information
}
